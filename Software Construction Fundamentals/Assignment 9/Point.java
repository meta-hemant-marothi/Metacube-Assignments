public class Point {
    private int x;
    private int y;

    /**
     * Creates a point object with given x and y coordinates.
     * @param x
     * @param y
     */
    Point(int x, int y){
        this.x = x;
        this.y = y;
    }

    public int getX(){
        return this.x;
    }

    public int getY(){
        return this.x;
    }
}
